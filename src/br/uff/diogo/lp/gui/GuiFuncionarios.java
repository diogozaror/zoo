    private void btnRemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemMouseClicked
    	Funcionario f = null;
        if(!lstCuidadores.isSelectionEmpty()){
            f = (Cuidador)lstCuidadores.getSelectedValue();
            FuncionarioManager.removeFuncionario(f.getID());
        }else if(!lstVeterinarios.isSelectionEmpty()){
            f = (Funcionario)lstVeterinarios.getSelectedValue();
            FuncionarioManager.removeFuncionario(f.getID());
        }
        
        lstCuidadores.setListData(FuncionarioManager.getCuidadores());
        lstVeterinarios.setListData(FuncionarioManager.getVeterinarios());
    }//GEN-LAST:event_btnRemMouseClicked

                                                

