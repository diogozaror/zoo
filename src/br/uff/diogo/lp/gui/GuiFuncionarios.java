    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        Funcionario f = null;
        boolean cuidador = false;
        if(!lstCuidadores.isSelectionEmpty()){
            //f = FuncionarioManager.getFuncionario((String)lstCuidadores.getSelectedValue());
            f = (Cuidador)lstCuidadores.getSelectedValue();
            cuidador = true;
        }else if(!lstVeterinarios.isSelectionEmpty()){
            //f = FuncionarioManager.getFuncionario((String)lstVeterinarios.getSelectedValue());
            f = (Veterinario)lstVeterinarios.getSelectedValue();
        }
        
        //if(f==null)return;
        
        new CadastroFuncionario(f, cuidador).setVisible(true);
    }//GEN-LAST:event_btnEditMouseClicked

